// Area of a circle: πr²
const radiusCircle = 5;
const circleArea = Math.PI * radiusCircle ** 2;
console.log(circleArea.toFixed(2));

// Area of a rectangle: length × width
const length = 10;
const width = 4;
const rectangleArea = length * width;
console.log(rectangleArea.toFixed(2));

// Volume of a cylinder: πr²h
const radiusCylinder = 3;
const height = 7;
const cylinderVolume = Math.PI * radiusCylinder ** 2 * height;
console.log(cylinderVolume.toFixed(2));
